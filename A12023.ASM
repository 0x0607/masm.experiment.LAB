.MODEL	SMALL
.DATA
	TEXT1		DB 'IBM PC ASSEMBER '
	TEXT2		DB 'IBM PC ASSENBER '
	LEN		DW 16
	DIFF		DB ?
	DIFF_ADDR	DW ?
.CODE
MAIN:
	MOV	AX,@DATA
	MOV	DS,AX
	LEA	SI,TEXT1
	LEA	DI,TEXT2
	DEC	SI
	DEC	DI
	MOV	CX,LEN
NEXT:
	INC 	SI
	INC 	DI
	MOV 	BH,[SI]
	MOV 	AH,[DI]
	CMP 	BH,AH
	LOOPE	NEXT
	;CMP	CX,0
	;JE	BREAK
	MOV	DIFF,AH
	MOV	BX,LEN
	SUB	BX,CX
	MOV	DIFF_ADDR,BX
BREAK:
	MOV AH,4CH
	INT 21H
.STACK
	END MAIN	
