.MODEL SMALL
.DATA
	TEXT DB "small word chang to big word",'$'
	LEN DW 28
.CODE
MAIN:
	MOV AX,@DATA
	MOV DS,AX
	LEA BX,TEXT
	MOV CX,LEN
NEXT:
	MOV DL,[BX]
	CMP DL,7AH
	JA OVER
	CMP DL,61H
	JB OVER
	AND DL,5FH
	MOV [BX],DL
OVER:	
	INC BX
	LOOP NEXT

	LEA TEXT,BX
	LEA DX,TEXT
	MOV AH,09H
	MOV AH,4CH
	INT 21H
.STACK
	END MAIN
