	.MODEL SMALL
	.DATA
		N	DB	7
	.CODE
	START:
	MOV	AX,@DATA
	MOV	DS,AX
	MOV	DL,N
	ADD	DL,'0'
	MOV	AH,02H
	INT	21H
	
	MOV	DL,'!'
	MOV	AH,02H
	INT	21H
	
	MOV	DL,'='
	MOV	AH,02H
	INT	21H

	MOV	AX,1
	MOV	CH,0
	MOV	CL,N
	CMP	N,0
	JE	AA

LP:	
	MUL	CX
	LOOP	LP

AA:	
	CMP	AX,10
	JL	ONE
	CMP	AX,100
	JL	TEN
	CMP	AX,1000
	JL	HUN

	MOV	CX,1000
	CWD

	DIV	CX
	MOV	CX,DX
	MOV	DL,AL
	ADD	DL,'0'
	MOV	AH,02H
	INT	21H
	MOV	AX,CX

HUN:
	MOV	DL,100
	DIV	DL
	MOV	BH,AH
	MOV	DL,AL
	ADD	DL,'0'
	MOV	AH,02H
	INT	21H
		
	MOV	AL,BH
	CBW

	TEN:
		MOV	DL,10
		DIV	DL
		MOV	BH,AH
		MOV	DL,AL
		ADD	DL,'0'
		MOV	AH,02H
		INT	21H
		MOV	AL,BH
		
	ONE:
		MOV	DL,AL
		ADD	DL,'0'
		MOV	AH,02H
		INT	21H
		MOV	AH,4CH
		INT	21H
		END	START
.STACK	