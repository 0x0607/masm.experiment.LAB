.MODEL	SMALL
.DATA
	MSG1 DB	0E1H
	MSG2 DB 0F8H
.CODE
MAIN:
	MOV	AX,@DATA
	MOV	DS,AX
	MOV	AL,MSG1		; AL = 0E1H
	MOV	BL,MSG2		; BL = 0F8H
	CMP	AL,BL		; Compare AL & BL
	JG	BIG		; SignedNumber AL > BL THEN GOTO BIG
	XCHG	AL,BL		; A exchanges with B
	MOV	MSG1,AL
	MOV	MSG2,BL
BIG:
	MOV AH,4CH
	INT 21H
.STACK
	END MAIN
