.MODEL	SMALL
.DATA
	MSG1	DB 44
	MSG2	DB 88
.CODE
MAIN:
	MOV	AX,@DATA
	MOV 	DS,AX
	MOV	AL,MSG1		; AL = 44
	MOV 	BL,MSG2 	; BL = 88
	CMP	AL,BL		; Compare AL & BL
	JB	SMALL		; IF AL < BL THEN GOTO SMALL
	XCHG	AL,BL		; A exchanges with B
	MOV	MSG1,AL
	MOV	MSG2,BL
SMALL:
	MOV	AH,4CH
	INT	21H
.STACK	
	END MAIN
