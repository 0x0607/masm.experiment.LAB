.MODEL	SMALL
.DATA
	TEXT	DB 'BIG WORD CHANG TO SMALL WORD'
	LEN 	DW 28
.CODE
MAIN:
	MOV	AX,@DATA
	MOV	DS,AX
	LEA	BX,TEXT
	MOV	CX,LEN
AGAIN:
	MOV	AH,[BX]
	CMP	AH,41H
	JB	NEXT
	CMP	AH,5AH
	JA	NEXT
	OR	AH,20H
	MOV	[BX],AH
NEXT:
	INC	BX
	LOOP	AGAIN
	MOV	AH,4CH
	INT	21H
.STACK
	END	MAIN
