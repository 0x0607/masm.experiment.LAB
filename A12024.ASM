.MODEL	SMALL
.DATA
	TEXT1		DB 'IBM PC ASSEMBER '
	TEXT2		DB 'A'
	LEN		DW 16
	CHAR_ADDR	DB ?
.CODE
MAIN:
	MOV	AX,@DATA
	MOV	DS,AX
	LEA	SI,TEXT1
	DEC	SI
	MOV	CX,LEN
NEXT:
	INC 	SI
	MOV 	DL,[SI]
	CMP 	TEXT2,DL
	LOOPNE	NEXT
	MOV	BX,LEN
	SUB	BX,CX
	MOV	CHAR_ADDR,BL
BREAK:
	MOV AH,4CH
	INT 21H
.STACK
	END MAIN	
