.MODEL	SMALL
.DATA
	TEXT	DB 'small word chang to big word'
	LEN 	DW 28
.CODE
MAIN:
	MOV	AX,@DATA
	MOV	DS,AX
	LEA	BX,TEXT
	MOV	CX,LEN
AGAIN:
	MOV	AH,[BX]
	CMP	AH,61H
	JB	NEXT
	CMP	AH,7AH
	JA	NEXT
	AND	AH,5FH
	MOV	[BX],AH
NEXT:
	INC	BX
	LOOP	AGAIN
	MOV	AH,4CH
	INT	21H
.STACK
	END	MAIN
