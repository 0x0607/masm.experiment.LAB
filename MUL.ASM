.MODEL	SMALL
.DATA
	NUM1	DB	8H
	NUM2 	DB	10H
	RE16 	DW	?
	NUM3 	DW	8000H
	NUM4 	DW	2H
	RE16L	DW	?
	RE16H	DW	?
.CODE
MAIN:
	MOV AX,@DATA
	MOV DS,AX
	MOV AL,NUM1
	MUL NUM2	; AL*NUM2 
	MOV RE16,AX	; result
	MOV AX,NUM3
	MUL NUM4	; AX*NUM4
	MOV RE16L,AX	; result (Least Significant Bit)
	MOV RE16H,DX	; result (Most Significant Bit)
	MOV AH,4CH
	INT 21H
.STACK
	END MAIN
